Obstacle::Obstacle(Vector2 position)
{
    this->position = position;

    for (unsigned int row = 0; row < grid.size(); ++row) {
        // Loops through each column of the current row
        for (unsigned int column = 0; column < grid[0].size(); ++column) {
            // Checks if the current grid cell is a solid block (1)
            if (grid[row][column] == 1) {
                float pos_x = position.x + column * 3; // Offset based on column index
                float pos_y = position.y + row * 3;    // Offset based on row index
                Barrier barrier = Barrier({ pos_x, pos_y });
                barriers.push_back(barrier);
            }
        }
    }
}

void Obstacle::DrawObstacle() {
    for (auto& barrier : barriers) {
        barrier.DrawBarrier();
    }
}